	<h3>Keywords</h3>
    <p>Keywords are the primary means by which information is received or distributed on the PheedBakk.</p>
<p>The way this works is that information is sent to you based on the keywords which you input into your keyword list on your keyword page.</p>

<p>What this implies is that if a pheed contains a Keyword which you specified, it will be displayed in your pheedstream.</p>

<p>You can add and remove keywords as you wish.</p>

<p>This means you can share all the information you want with friends, family and colleagues just using words.</p>

<p>Just add a keyword and start sharing. Thats it. You can create unique keywords to share information within a group.</p>
<p>Seriously, it's That easy.</p>
